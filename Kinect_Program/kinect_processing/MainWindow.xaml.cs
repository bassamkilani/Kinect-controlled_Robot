        /// <summary>
        /// Width of output drawing
        /// </summary>
        private const float RenderWidth = 640.0f;

        /// <summary>
        /// Height of our output drawing
        /// </summary>
        private const float RenderHeight = 480.0f;

        /// <summary>
        /// Thickness of drawn joint lines
        /// </summary>
        private const double JointThickness = 3;

        /// <summary>
        /// Thickness of body center ellipse
        /// </summary>
        private const double BodyCenterThickness = 10;

        /// <summary>
        /// Thickness of clip edge rectangles
        /// </summary>
        private const double ClipBoundsThickness = 10;

        /// <summary>
        /// Brush used to draw skeleton center point
        /// </summary>
        private readonly Brush centerPointBrush = Brushes.Blue;

        /// <summary>
        /// Brush used for drawing joints that are currently tracked
        /// </summary>
        private readonly Brush trackedJointBrush = new SolidColorBrush(Color.FromArgb(255, 68, 192, 68));

        /// <summary>
        /// Brush used for drawing joints that are currently inferred
        /// </summary>        
        private readonly Brush inferredJointBrush = Brushes.Yellow;

        /// <summary>
        /// Pen used for drawing bones that are currently tracked
        /// </summary>
        private readonly Pen trackedBonePen = new Pen(Brushes.Green, 6);

        /// <summary>
        /// Pen used for drawing bones that are currently inferred
        /// </summary>        
        private readonly Pen inferredBonePen = new Pen(Brushes.Gray, 1);

        private KinectSensor sensor;

        /// <summary>
        /// Drawing group for skeleton rendering output
        /// </summary>
        private DrawingGroup drawingGroup;

        /// <summary>
        /// Drawing image that we will display
        /// </summary>
        private DrawingImage imageSource;

        public SerialPort sp = new SerialPort();

        private int counter = 0;

        public MainWindow()
        {

            InitializeComponent();
            Loaded += MainWindowLoaded;
        }

        private void MainWindowLoaded(object sender, RoutedEventArgs e)
        {
            sp.BaudRate = 9600;
            var sensorStatus = new KinectSensorChooser();

            sensorStatus.KinectChanged += KinectSensorChooserKinectChanged;

            sensorStatus.Start();

        }

        private void KinectSensorChooserKinectChanged(object sender, KinectChangedEventArgs e)
        {
            foreach (var potentialSensor in KinectSensor.KinectSensors)
            {

                if (e.NewSensor.Status == KinectStatus.Connected)
                {
                    this.sensor = e.NewSensor;
                    break;
                }
            }

            if (null != this.sensor)
            {
                // Turn on the skeleton stream to receive skeleton frames
                this.sensor.SkeletonStream.Enable();

                // Add an event handler to be called whenever there is new color frame data
                this.sensor.SkeletonFrameReady += this.SensorSkeletonFrameReady;
                this.sensor.SkeletonFrameReady += KinectSkeletonFrameReady;
            }

            if (sensor == null)
                return;

            switch (Convert.ToString(e.NewSensor.Status))
            {
                case "Connected":
                    kStatus.Content = "Connected";
                    break;
                case "Disconnected":
                    kStatus.Content = "Disconnected";
                        break;
                case "Error":
                    kStatus.Content = "Error";
                    break;
                case "NotReady":
                    kStatus.Content = "Not Ready";
                    break;
                case "NotPowered":
                    kStatus.Content = "Not Powered";
                    break;
                case "Initializing":
                    kStatus.Content = "Initialising";
                    break;
                default: kStatus.Content = "Undefined"; break;
            }
        }

        private void KinectSkeletonFrameReady(object sender, SkeletonFrameReadyEventArgs e)
        {
            var skeletons = new Skeleton[0];

            using (var skeletonFrame = e.OpenSkeletonFrame())
            {
                if (skeletonFrame != null)
                {
                    skeletons = new Skeleton[skeletonFrame.SkeletonArrayLength];
                    skeletonFrame.CopySkeletonDataTo(skeletons);
                }

            }

            if (skeletons.Length == 0)
            {
                return;
            }

            var skel = skeletons.FirstOrDefault(x => x.TrackingState == SkeletonTrackingState.Tracked);
            if (skel == null)
            {
                return;
            }

            var rightHand = skel.Joints[JointType.WristRight];
            var leftHand = skel.Joints[JointType.WristLeft];

            if (counter >= 15)
            {
                byte[] rcvd = new byte[14];
                // right hand - clamp control - X-Axis
                XValueRight.Text = rightHand.Position.X.ToString(CultureInfo.InvariantCulture);
                var sentRX = '!' + XValueRight.Text + '\n';
                sp.Write(sentRX);
                System.Diagnostics.Debug.WriteLine("\nSENT R-X: " + sentRX);

                // right hand - base rotation - Y-Axis
                YValueRight.Text = rightHand.Position.Y.ToString(CultureInfo.InvariantCulture);
                var sentRY = '@' + YValueRight.Text + '\n';
                sp.Write(sentRY);
                System.Diagnostics.Debug.WriteLine("\nSENT R-Y: " + sentRY);

                ZValueRight.Text = rightHand.Position.Z.ToString(CultureInfo.InvariantCulture);

                // left hand - base rotation - X-Axis
                XValueLeft.Text = leftHand.Position.X.ToString(CultureInfo.InvariantCulture);
                var sentLX = '#' + XValueLeft.Text + '\n';
                sp.Write(sentLX);
                System.Diagnostics.Debug.WriteLine("\nSENT L-X: " + sentLX);

                YValueLeft.Text = leftHand.Position.Y.ToString(CultureInfo.InvariantCulture);
                var sentLY = '$' + YValueLeft.Text + '\n';
                sp.Write(sentLY);
                System.Diagnostics.Debug.WriteLine("\nSENT L-Y: " + sentLY);

                ZValueLeft.Text = leftHand.Position.Z.ToString(CultureInfo.InvariantCulture);

                counter = 0;
            }

            counter++;

            var centreHip = skel.Joints[JointType.HipCenter];

            if (centreHip.Position.Z - rightHand.Position.Z > 0.35)
            {
                RightRaised.Text = "Raised";
            }
            else if (centreHip.Position.Z - leftHand.Position.Z > 0.35)
            {
                LeftRaised.Text = "Raised";
            }
            else
            {
                LeftRaised.Text = "Lowered";
                RightRaised.Text = "Lowered";
            }

        }



        /// <summary>
        /// Draws indicators to show which edges are clipping skeleton data
        /// </summary>
        /// <param name="skeleton">skeleton to draw clipping information for</param>
        /// <param name="drawingContext">drawing context to draw to</param>
        private static void RenderClippedEdges(Skeleton skeleton, DrawingContext drawingContext)
        {
            if (skeleton.ClippedEdges.HasFlag(FrameEdges.Bottom))
            {
                drawingContext.DrawRectangle(
                    Brushes.Red,
                    null,
                    new Rect(0, RenderHeight - ClipBoundsThickness, RenderWidth, ClipBoundsThickness));
            }

            if (skeleton.ClippedEdges.HasFlag(FrameEdges.Top))
            {
                drawingContext.DrawRectangle(
                    Brushes.Red,
                    null,
                    new Rect(0, 0, RenderWidth, ClipBoundsThickness));
            }

            if (skeleton.ClippedEdges.HasFlag(FrameEdges.Left))
            {
                drawingContext.DrawRectangle(
                    Brushes.Red,
                    null,
                    new Rect(0, 0, ClipBoundsThickness, RenderHeight));
            }

            if (skeleton.ClippedEdges.HasFlag(FrameEdges.Right))
            {
                drawingContext.DrawRectangle(
                    Brushes.Red,
                    null,
                    new Rect(RenderWidth - ClipBoundsThickness, 0, ClipBoundsThickness, RenderHeight));
            }
        }

        private void WindowLoaded(object sender, RoutedEventArgs e)
        {
            // Create the drawing group we'll use for drawing
            this.drawingGroup = new DrawingGroup();

            // Create an image source that we can use in our image control
            this.imageSource = new DrawingImage(this.drawingGroup);

            // Display the drawing using our image control
            Image.Source = this.imageSource;
        }



        /// <summary>
        /// Execute shutdown tasks
